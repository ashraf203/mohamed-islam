<?PHP

// add

// multiply

// substract

// div




function add($number1, $number2)
{
    return $number1 + $number2;
}

function multiply($number1, $number2)
{
    return $number1 * $number2;
}

function substract($number1, $number2)
{
    return $number1 - $number2;
}

function div($number1, $number2)
{
    return $number1 / $number2;
}
